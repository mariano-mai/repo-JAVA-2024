package javajedi2024.n2.padawan;

public class ConviertoCosas2 {

	public static void main(String[] args) {
		
		int contador = 0;
		
		System.out.println("BUCLE WHILE");
		
		while(contador <= 5) {
			System.out.println(contador);
			contador++;
		}
		
		
		System.out.println("BUCLE FOR");
		
		for(int i=0; i<=5; i++) {
			System.out.println(i);
		}

	}

}
