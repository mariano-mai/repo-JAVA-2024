package javajedi2024.n2.padawan;

public class ConviertoCosas {

	public static void main(String[] args) {
		
		System.out.println("BUCLE WHILE");
		
		int contador = 0;
		
		while(contador <=5) {
			System.out.println(contador);
			contador++;
		}
		
		contador = 0;
		
		System.out.println("BUCLE DO-WHILE");
		
		do {
			System.out.println(contador);
			contador++;
		}
		while(contador<=5);

	}

}
