package javajedi2024.n1.iniciado;

public class Multiplicacion {

	public static void main(String[] args) {
		
		double valor1 = 3.543d;
		double valor2 = 9.2d;
		
		double producto = valor1*valor2;
		
		int valor3 = 34;
		int valor4 = 5;
		
		int producto2 = valor3*valor4;
		
		double suma = producto+producto2;
		
		System.out.println("Resultado:"+suma);
		System.out.println("Va a ser real, porque parte de las variables que lo componen son reales.");

	}

}
