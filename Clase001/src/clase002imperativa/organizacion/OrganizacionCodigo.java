package clase002imperativa.organizacion;

public class OrganizacionCodigo {

	public static void main(String[] args) {
		System.out.println("Línea de código.");
		System.out.println("Otra línea de código.");
		
		//declaraciones
		int nombreVariable;
		
		//asignaciones
		nombreVariable = 1;
		nombreVariable = 2;
		
		//declaración y asignación
		int nombreVariable2 = 15;
		
		//declaro e inicializo varias variables
		int valor1, valor2 = 4;
		valor1 = nombreVariable;
		System.out.println("nombreVariable: "+nombreVariable);
		System.out.println("nombreVAriable2: "+nombreVariable2);
		System.out.println("Valores 1 y 2, respectivamente: "+valor1+"; "+valor2);

	}

}
